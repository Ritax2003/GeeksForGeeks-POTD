class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        multiset<int>s;
        for(auto i :nums){
            s.insert(i);
        }
        nums.clear();
        for(auto k:s){
            if(s.count(k)==1) nums.push_back(k);
        }
        return nums;
    }
};

