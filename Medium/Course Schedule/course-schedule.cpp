class Solution
{
  public:
    vector<int> findOrder(int n, int m, vector<vector<int>> prerequisites) 
    {
        vector<vector<int>> adj(n, vector<int>());
        vector<int> inDegree(n, 0);

        // Build adjacency list and calculate in-degrees
        for (int i = 0; i < m; ++i) {
            int u = prerequisites[i][1];
            int v = prerequisites[i][0];
            adj[u].push_back(v);
            inDegree[v]++;
        }

        // Create a queue for topological sorting
        queue<int> q;
        for (int i = 0; i < n; ++i) {
            if (inDegree[i] == 0) {
                q.push(i);
            }
        }

        vector<int> result;
        while (!q.empty()) {
            int u = q.front();
            q.pop();
            result.push_back(u);

            for (int v : adj[u]) {
                if (--inDegree[v] == 0) {
                    q.push(v);
                }
            }
        }

        // Check if there is a valid ordering
        if (result.size() == n) {
            return result;
        } else {
            // If not possible to finish all tasks, return an empty array
            return {};
        }
    }
};

